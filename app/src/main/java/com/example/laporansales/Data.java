package com.example.laporansales;

public class Data {
    private String Tanggal, Area, Hitam, Classic, Coklat, Merah, Jawas, Jumlah, Setor,Titip, Outlet, Alamat, Depo;

    public Data() {

    }

    public Data(String tanggal, String area, String hitam, String classic, String coklat, String merah, String jawas, String jumlah, String setor, String titip, String outlet, String alamat, String depo) {
        Tanggal = tanggal;
        Area = area;
        Hitam = hitam;
        Classic = classic;
        Coklat = coklat;
        Merah = merah;
        Jawas = jawas;
        Jumlah = jumlah;
        Setor = setor;
        Titip = titip;
        Outlet = outlet;
        Alamat = alamat;
        Depo = depo;
    }

    public String getTanggal() {
        return Tanggal;
    }

    public void setTanggal(String tanggal) {
        Tanggal = tanggal;
    }

    public String getArea() {
        return Area;
    }

    public void setArea(String area) {
        Area = area;
    }

    public String getHitam() {
        return Hitam;
    }

    public void setHitam(String hitam) {
        Hitam = hitam;
    }

    public String getClassic() {
        return Classic;
    }

    public void setClassic(String classic) {
        Classic = classic;
    }

    public String getCoklat() {
        return Coklat;
    }

    public void setCoklat(String coklat) {
        Coklat = coklat;
    }

    public String getMerah() {
        return Merah;
    }

    public void setMerah(String merah) {
        Merah = merah;
    }

    public String getJawas() {
        return Jawas;
    }

    public void setJawas(String jawas) {
        Jawas = jawas;
    }

    public String getJumlah() {
        return Jumlah;
    }

    public void setJumlah(String jumlah) {
        Jumlah = jumlah;
    }

    public String getSetor() {
        return Setor;
    }

    public void setSetor(String setor) {
        Setor = setor;
    }

    public String getTitip() {
        return Titip;
    }

    public void setTitip(String titip) {
        Titip = titip;
    }

    public String getOutlet() {
        return Outlet;
    }

    public void setOutlet(String outlet) {
        Outlet = outlet;
    }

    public String getAlamat() {
        return Alamat;
    }

    public void setAlamat(String alamat) {
        Alamat = alamat;
    }

    public String getDepo() {
        return Depo;
    }

    public void setDepo(String depo) {
        Depo = depo;
    }
}

